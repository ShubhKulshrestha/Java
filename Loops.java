//Loops in Java...
//For Loop 
public class demo{
  public static void main(String args[]){
    int i;
    for(i=1;i<=10;i++){
      System.out.println(i);
    }
  }
}
//While Loop
public class demo{
  public static void main(String args[]){
    int i=1;
    while(i<10){
      System.out.println(i);
      i++;
    }
  }
}
